// Include the required libraries to run our code:
#include <iostream>
using namespace std; // Optional: to avoid writing std:: before every standard library usage
// The main entry point to run our program: main()
int main() {
    //  for printing anything to the terminal: std::cout
    // std::endl is used to insert a new line
    cout << "Hello, World!" << endl;
    cout << "Welcome to C++ programming." << endl;
    // let's print one more lines
    // in python we used print() function
    // in C++ we use std::cout
    std::cout << "We are learning C++ programming together.";
    std::cout << "Learning C++ is fun!" << std::endl;
    // return 0 to indicate that our program ended successfully
    return 0;
    // returning any other value would indicate that our program ended with an error
}